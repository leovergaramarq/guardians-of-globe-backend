-- Setup tables

CREATE TABLE GUARDIAN.person (
    person_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    person_name VARCHAR2(50),
    birthdate DATE,
    sex CHAR(1),
    occupation VARCHAR2(50),
    address VARCHAR2(100),
    -- constraints
    CONSTRAINT person_pk PRIMARY KEY (person_id)
);

CREATE TABLE GUARDIAN.schedule_event (
    schedule_event_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    person_id NUMBER NOT NULL,
    event_name VARCHAR2(50),
    date_start DATE,
    date_end DATE,
    -- constraints
    CONSTRAINT schedule_event_pk PRIMARY KEY (schedule_event_id),
    CONSTRAINT schedule_event_person_fk FOREIGN KEY (person_id) REFERENCES GUARDIAN.person (person_id)
);

CREATE TABLE GUARDIAN.relationship (
    relationship_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    person1_id NUMBER NOT NULL,
    person2_id NUMBER NOT NULL,
    relationship_type VARCHAR2(25), -- relationship in terms of person1 to person2
    -- constraints
    CONSTRAINT relationship_pk PRIMARY KEY (relationship_id),
    CONSTRAINT relationship_person1_fk FOREIGN KEY (person1_id) REFERENCES GUARDIAN.person (person_id),
    CONSTRAINT relationship_person2_fk FOREIGN KEY (person2_id) REFERENCES GUARDIAN.person (person_id)
);

CREATE TABLE GUARDIAN.alter_ego (
    alter_ego_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    person_id NUMBER NOT NULL,
    origin VARCHAR2(50),
    is_hero NUMBER(1),
    alias VARCHAR2(50),
    -- constraints
    CONSTRAINT alter_ego_pk PRIMARY KEY (alter_ego_id),
    CONSTRAINT alter_ego_person_fk FOREIGN KEY (person_id) REFERENCES GUARDIAN.person (person_id)
);

CREATE TABLE GUARDIAN.alter_ego_weakness (
    alter_ego_weakness_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    alter_ego_id NUMBER NOT NULL,
    weakness_name VARCHAR2(50),
    -- constraints
    CONSTRAINT alter_ego_weakness_pk PRIMARY KEY (alter_ego_weakness_id),
    CONSTRAINT alter_ego_weakness_alter_ego_fk FOREIGN KEY (alter_ego_id) REFERENCES GUARDIAN.alter_ego (alter_ego_id)
);

CREATE TABLE GUARDIAN.alter_ego_ability (
    alter_ego_ability_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    alter_ego_id NUMBER NOT NULL,
    ability_name VARCHAR2(50),
    -- constraints
    CONSTRAINT alter_ego_ability_pk PRIMARY KEY (alter_ego_ability_id),
    CONSTRAINT alter_ego_ability_alter_ego_fk FOREIGN KEY (alter_ego_id) REFERENCES GUARDIAN.alter_ego (alter_ego_id)
);

CREATE TABLE GUARDIAN.fight (
    fight_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    date_start DATE NOT NULL,
    date_end DATE NOT NULL,
    location VARCHAR2(50) NOT NULL,
    fight_title VARCHAR2(50) NOT NULL,
    description VARCHAR2(400),
    -- constraints
    CONSTRAINT fight_pk PRIMARY KEY (fight_id)
);

CREATE TABLE GUARDIAN.alter_ego_fight (
    alter_ego_fight_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    alter_ego_id NUMBER NOT NULL,
    fight_id NUMBER NOT NULL,
    victory NUMBER(1),
    side NUMBER(1), -- just a way to differentiate sides
    -- constraints
    CONSTRAINT alter_ego_fight_pk PRIMARY KEY (alter_ego_fight_id),
    CONSTRAINT alter_ego_fight_alter_ego_fk FOREIGN KEY (alter_ego_id) REFERENCES GUARDIAN.alter_ego (alter_ego_id),
    CONSTRAINT alter_ego_fight_fight_fk FOREIGN KEY (fight_id) REFERENCES GUARDIAN.fight (fight_id)
);

CREATE TABLE GUARDIAN.sponsor_source (
    sponsor_source_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    sponsor_source_name VARCHAR2(50),
    reliability VARCHAR2(50),
    -- constraints
    CONSTRAINT sponsor_source_pk PRIMARY KEY (sponsor_source_id)
);

CREATE TABLE GUARDIAN.sponsor (
    sponsor_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    sponsor_source_id NUMBER,
    sponsor_name VARCHAR2(50),
    -- constraints
    CONSTRAINT sponsor_pk PRIMARY KEY (sponsor_id),
    CONSTRAINT sponsor_sponsor_source_fk FOREIGN KEY (sponsor_source_id) REFERENCES GUARDIAN.sponsor_source (sponsor_source_id)
);

CREATE TABLE GUARDIAN.sponsorship (
    sponsorship_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    sponsor_id NUMBER NOT NULL,
    alter_ego_id NUMBER NOT NULL,
    -- constraints
    CONSTRAINT sponsorship_pk PRIMARY KEY (sponsorship_id),
    CONSTRAINT sponsorship_sponsor_fk FOREIGN KEY (sponsor_id) REFERENCES GUARDIAN.sponsor (sponsor_id),
    CONSTRAINT sponsorship_alter_ego_fk FOREIGN KEY (alter_ego_id) REFERENCES GUARDIAN.alter_ego (alter_ego_id)
);

